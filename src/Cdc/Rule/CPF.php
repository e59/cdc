namespace Cdc\Rule;

class CPF extends Rule {

    public function check($index, &$row, $definition = null, $rowset = array()) {
        if (!isset($row[$index]) || !$row[$index]) {
            return array();
        }

        $row[$index] = str_pad(preg_replace('#\D#', '', $row[$index]), 11, '0', STR_PAD_LEFT);

        if (self::validate($row[$index])) {
            return array();
        }

        return array('CPF inválido.');
    }

    public static function validate($cpf) {
        $basic_check = strlen($cpf) != 11 || $cpf == '00000000000' || $cpf == '11111111111' || $cpf == '22222222222' || $cpf == '33333333333' || $cpf == '44444444444' || $cpf == '55555555555' || $cpf == '66666666666' || $cpf == '77777777777' || $cpf == '88888888888' || $cpf == '99999999999';

        if ($basic_check) {
            return false;
        }
        for ($t = 9; $t < 11; $t++) {
            for ($d = 0, $c = 0; $c < $t; $c++) {
                $d += $cpf{$c} * (($t + 1) - $c);
            }

            $d = ((10 * $d) % 11) % 10;

            if ($cpf{$c} != $d) {
                return false;
            }
        }

        return true;
    }

}
